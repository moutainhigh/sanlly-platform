package com.sanlly.production.models.output.app.base;

/**
 * app端尺寸下拉选输出类
 * @author zhangkai
 *
 */
public class SizeSelectOutput {
	
	private String key;
	
	private String sizeName;
	
	private String sizeNameEn;

	public String getKey() {
		return key;
	}

	public void setKey(String key) {
		this.key = key;
	}

	public String getSizeName() {
		return sizeName;
	}

	public void setSizeName(String sizeName) {
		this.sizeName = sizeName;
	}

	public String getSizeNameEn() {
		return sizeNameEn;
	}

	public void setSizeNameEn(String sizeNameEn) {
		this.sizeNameEn = sizeNameEn;
	}
	
	

}
