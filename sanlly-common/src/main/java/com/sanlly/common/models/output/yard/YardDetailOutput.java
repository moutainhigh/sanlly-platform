public class YardDetailOutput {

	private Integer yardId;
	
	private String yardKey;
	
	private String yardName;
	
	private String yardNameEn;
	
	private String companyKey;

	public Integer getYardId() {
		return yardId;
	}

	public void setYardId(Integer yardId) {
		this.yardId = yardId;
	}

	public String getYardName() {
		return yardName;
	}

	public void setYardName(String yardName) {
		this.yardName = yardName;
	}

	public String getCompanyKey() {
		return companyKey;
	}

	public void setCompanyKey(String companyKey) {
		this.companyKey = companyKey;
	}

	public String getYardKey() {
		return yardKey;
	}

	public void setYardKey(String yardKey) {
		this.yardKey = yardKey;
	}

	public String getYardNameEn() {
		return yardNameEn;
	}

	public void setYardNameEn(String yardNameEn) {
		this.yardNameEn = yardNameEn;
	}
	
	
}
