package com.sanlly.common.models.output;

public class DeptOutput {

	private Integer prodDeptId;
	
	private String key;

    private String keyNameLang;

    private String company;

    private String deptName;

    private String deptNameEn;

    private Integer deptType;

	public Integer getProdDeptId() {
		return prodDeptId;
	}

	public void setProdDeptId(Integer prodDeptId) {
		this.prodDeptId = prodDeptId;
	}

	public String getKey() {
		return key;
	}

	public void setKey(String key) {
		this.key = key;
	}

	public String getKeyNameLang() {
		return keyNameLang;
	}

	public void setKeyNameLang(String keyNameLang) {
		this.keyNameLang = keyNameLang;
	}

	public String getCompany() {
		return company;
	}

	public void setCompany(String company) {
		this.company = company;
	}

	public String getDeptName() {
		return deptName;
	}

	public void setDeptName(String deptName) {
		this.deptName = deptName;
	}

	public String getDeptNameEn() {
		return deptNameEn;
	}

	public void setDeptNameEn(String deptNameEn) {
		this.deptNameEn = deptNameEn;
	}

	public Integer getDeptType() {
		return deptType;
	}

	public void setDeptType(Integer deptType) {
		this.deptType = deptType;
	}
    
    
}
